        if 'C' in self.game.tilemap[y][x]:
            self.image = self.game.obj_spritesheet.get_sprite(198, 768, self.width, self.height)
        self.rect = self.image.get_rect()
        self.rect.x = self.x
        self.rect.y = self.y

